→便利なツールのアップデートが頻繁に行われているWEB業界委において、
Gitを活用して業務を効率化させることは一般的になってきています。
Gitを活用することで、チームでの作業効率を高めたり、多くのファイル
を管理することが簡単になります。
Gitとは、分散型バージョン管理システムです。
ざっくりいうとファイルのバージョン管理が簡単にできるツールと
いえます。基本的には、最新バージョンにアップデートしたりすると
古いバージョンに戻すことは難しいです。
しかし、Gitで管理しているファイルであれば、コンピュータ上で
ファイルの編集履歴を管理できるので、編集前のファイルを残したまま、
新しく編集したファイルを保存することができます。
なので古いバージョンから新しいバージョンまでの管理が簡単です。
Gitでできること。<br>
・古いバージョンに簡単に戻せる。<br>
・新旧のファイルを一元管理できる。<br>
・編集した履歴を複数人で共有できる。<br>
・複数人で修正した部分を一つに統合できる。<br>
