→仕様書とは、工業製品やソフトウェア、情報システム、サービスなどを開発
する際に、備えるべき機能やその性能、特性、満たすべき要件などを図表や
文章などで記述した文書。
何を作るべきかを明確にして開発者間で共有するために作成される。
情報システムの受託開発のように発注者との受注者が分かれている場合は、
金額や期間の算定根拠夜行書の前提条件ともなるため、両者間で齟齬がないように
入念に作り込んでいくことが多い。<br>
