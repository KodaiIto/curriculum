→マージツールとは、FDLファイルまたはDTDファイルの変更内容を
MDLファイルにマージできるものを言います。マージツールには四つの
機能があります。<br>
（１）マージ機能<br>
（２）マップ機能<br>
（３）マッピング機能<br>
（４）フォーマット挿入機能<br>
※FDL：GNU Free Documentation Licenceの略。マニュアル、
教科書などの文書類の複製、再配布を自由にできるようにするために提唱
されたライセンス。FDLの各条項に従う限りにおいて、文書の変更、翻訳、
他の文書との結合、複数のFDL文書の集合体の作成などを行うことが
できる。<br>
※DTD：とは「文書型定義」のことで、SGMLやXMLの文書構造を定義する
スキーマ言語の一つ。<br>
※MDL：Material Definition Languageの略。<br>
※拡張子（filename extension）：ファイルの種別を識別するために
ファイルの名前（ファイル名）の末尾に付けられる文字列。<br>
